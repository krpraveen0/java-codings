class Operators{
	public static void main(String[] args){
		int num = 10;
		System.out.println("The number is " + num);
		System.out.println("The post increased number is" + num++);
		System.out.println("the pre increased number is " + ++num);
		System.out.println("the post decreased number is " + num-- );
		System.out.println("the pre decreased number is " + --num );
		System.out.println("the number is : " + num);
		
	}
}