class CharSwitch{
	public static void main(String[] args){
		char c = 'C';
		switch(c){
			case 'A' :
				System.out.println("The output is : " + c);
				break;
			case 'B' :
				System.out.println("The output is : " + c);
				break;
			case 'C' :
				System.out.println("The output is : " + c);
				break;
			case 'D' :
				System.out.println("The output is : " + c);
				break;
			case 'E' :
				System.out.println("The output is : " + c);
				break;
			default:
				System.out.println("Please enter a valid character...");
		}
	}
}