class OverloadMainMethod{
	public static void main(String[] args){
		System.out.println("main method with String[] array params");
	}
	public static void main(int[] args){
		System.out.println("main method with int[] array params");
	}
	public static void main(float[] args){
		System.out.println("main method with float[] array params");
	}
	
}