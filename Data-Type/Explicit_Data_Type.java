class ExplicitDataType{
	public static void main(String[] args){
		int i = 10;
		//explicit type casting
		byte b = (byte)i;
		//explicit type casting
		short s = (byte)i;
		System.out.println("The converted byte type data is : " + b);
		System.out.println("The converted short type data is : " + s);
	}
}